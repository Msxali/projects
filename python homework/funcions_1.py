def sum (num1, num2):
    answer = num1 + num2
    return answer

def minus (num1, num2):
    answer = num1 - num2
    return answer

def divide (num1, num2):
    answer = num1 / num2
    return answer

def mult (num1, num2):
    answer = num1 * num2
    return answer

print(sum(19,10))
print(minus(19,10))
print(divide(19,10))
print(mult(19, 19))